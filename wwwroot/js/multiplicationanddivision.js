function multiplication() {
    var a = Number(document.getElementById("a").value);
    var b = Number(document.getElementById("b").value);
    document.getElementById("result").innerHTML = a * b;
}

function division() {
    var a = Number(document.getElementById("a").value);
    var b = Number(document.getElementById("b").value);
    document.getElementById("result").innerHTML = a / b;
}